namespace Overloading
{
    public class TinhToan
    {
        //Tạo ra phương thức tĩnh để cộng
        public static int Congn(int a, int b)
        {return a+b;}
        public static int Cong(int a, int b, int c)
        {return a+b+c;}
        public static double Cong(double a, double b)
        {return a+b;}
        public static string Cong(string a, string b)
        {return a+b;}
    }
}